
public class VeriTipiDegiskenler {

    public static void main(String[] args) {

        boolean cevap = false;
        System.out.println(cevap);
        cevap = true;
        System.out.println(cevap);

        byte yas = 100;
        System.out.println(yas);
        short rastgeleSayi = 5000;
        System.out.println(rastgeleSayi);
        int ayniRakamliSayi = 111111111;
        System.out.println(ayniRakamliSayi);
        long tckn = 11111111111L;
        System.out.println(tckn);

        float ondalikSayi = 10.5F;
        System.out.println(ondalikSayi);
        float ondalikSayi2 = 10;
        System.out.println(ondalikSayi2);
        double buyukOndalikliSayi = 2237238423748324742842837432454385345435342734.5D;
        System.out.println(buyukOndalikliSayi);

        char karakterDegiskeni = '5';
        System.out.println(karakterDegiskeni);
        
        String kursAdi = "Kapsamli Java Eğitimi";
        System.out.println(kursAdi);

    }
}

